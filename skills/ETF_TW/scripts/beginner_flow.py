#!/usr/bin/env python3
from __future__ import annotations


def beginner_topic(topic: str) -> str:
    topics = {
        "basics": BASICS,
        "choose-style": CHOOSE_STYLE,
        "first-trade": FIRST_TRADE,
        "pitfalls": PITFALLS,
        "risk": RISK,
    }
    if topic not in topics:
        raise KeyError(topic)
    return topics[topic]


BASICS = """ETF 基礎

- ETF 是一籃子資產的基金，可以像股票一樣在台灣證券市場交易。
- 對新手來說，ETF 的核心價值通常是分散持股、降低單一公司風險。
- 但 ETF 不是自動安全：不同 ETF 仍有市場、利率、匯率、產業集中等風險。

入門順序建議：
1. 先分清自己要的是長期成長、現金流，還是資產配置。
2. 再決定先研究市值型、高股息、債券，還是海外型。
3. 在搞懂交易成本與下單方式之前，不要因為熱門就直接買。"""


CHOOSE_STYLE = """如何選 ETF 風格

如果你主要目標是長期累積資產：
- 先看 broad-market 類型，通常更適合做核心配置。

如果你主要目標是現金流感受：
- 可以研究 high-dividend 類型，但不要把高殖利率直接等同低風險。

如果你想做股債配置：
- 可以研究 bond 類型，但要先理解利率敏感度。

如果你特別想投科技或海外題材：
- 通常比較適合當衛星部位，不建議新手一開始就全壓。

簡化判斷：
- 想穩定打底：先看市值型
- 想現金流：再看高股息
- 想配置避震：研究債券型
- 想提高成長性：小比例看海外/主題型"""


FIRST_TRADE = """第一筆交易前準備

先確認這 8 件事：
1. 證券戶已核准
2. 交割戶已綁定完成
3. 你知道資金要先放在哪裡
4. 你分得清整股與零股
5. 你知道這檔 ETF 為什麼適合你
6. 你知道大概手續費與交易稅影響
7. 你不是因為新聞熱度才想買
8. 你願意接受買進後短期波動

對多數新手來說：
- 如果你怕一次買在高點，可以先用定期定額
- 如果你想先熟悉流程，可以先從小額零股開始
- 如果你連 ETF 類型都還分不清，先不要急著下第一筆"""


PITFALLS = """新手常見陷阱

- 只看配息，不看總報酬
- 把月配息誤認為比較厲害
- 忽略 ETF 的內扣費用
- 看到熱門代碼就追
- 把主題型或產業型 ETF 當成全部核心配置
- 沒搞懂折溢價就買進
- 因為短期漲跌就頻繁換股

簡單原則：
- 先搞懂，再投入
- 先有策略，再選代碼
- 先看適不適合自己，不要只看別人買什麼"""


RISK = """ETF 風險提醒

ETF 不是保本工具。
你至少要知道這幾種風險：

- 市場風險：大盤跌時，ETF 也可能跟著跌
- 集中風險：主題型 ETF 可能過度集中在少數產業或公司
- 匯率風險：海外型 ETF 會受到新台幣匯率影響
- 利率風險：債券 ETF 對利率變動很敏感
- 現金流錯覺：配息不代表真的賺比較多

真正重要的是：
- 這檔 ETF 是否符合你的目標
- 你是否能承受它的波動
- 你是否知道自己為什麼持有它"""
